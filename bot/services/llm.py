_client: AsyncGroq | None = None


def init_llm() -> None:
    global _client
    api_key = os.getenv("GROQ_API_KEY")
    if not api_key:
        raise ValueError("GROQ_API_KEY не указан в .env")
    _client = AsyncGroq(api_key=api_key)


def random_topic() -> str:
    return random.choice(TOPICS)


async def generate_words(topic: str, count: int = 20) -> list[dict]:
    """
    Запрашивает у Groq список слов на тему с определениями.
    Возвращает [{"word": "СЛОВО", "clue": "Определение"}, ...]
    """
    prompt = f"""Ты составляешь кроссворд на русском языке на тему: «{topic}».

Дай ровно {count} слов для кроссворда. Требования к словам:
- Только имена существительные в именительном падеже единственного числа
- Длина от 4 до 9 букв
- Только русские буквы, без дефисов и пробелов
- Слова должны быть связаны с темой
- Слова не должны повторяться

Для каждого слова дай краткое определение в стиле кроссворда (одно предложение, без указания самого слова).

Ответь СТРОГО в формате JSON, без пояснений, без markdown, только массив:
[
  {{"word": "СЛОВО", "clue": "Краткое определение"}},
  ...
]"""

    try:
        response = await _client.chat.completions.create(
            model="llama-3.3-70b-versatile",
            messages=[{"role": "user", "content": prompt}],
            temperature=0.7,
            max_tokens=2000,
        )
        text = response.choices[0].message.content.strip()

        text = re.sub(r"```json\s*", "", text)
        text = re.sub(r"```\s*", "", text)
        text = text.strip()

        data = json.loads(text)

        result = []
        seen = set()
        for item in data:
            word = item.get("word", "").upper().strip()
            clue = item.get("clue", "").strip()

            if not word or not clue:
                continue
            if not re.match(r"^[А-ЯЁ]{4,9}$", word):
                continue
            if word in seen:
                continue

            seen.add(word)
            result.append({"word": word, "clue": clue})

        logger.info(f"Groq вернул {len(result)} валидных слов на тему «{topic}»")
        return result

    except json.JSONDecodeError as e:
        logger.error(f"Ошибка парсинга JSON от Groq: {e}\nОтвет: {text[:500]}")
        return []
    except Exception as e:
        logger.error(f"Ошибка запроса к Groq: {e}")
        return []